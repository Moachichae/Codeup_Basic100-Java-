package basicProblem100.solving02;

import java.util.Scanner;

public class CodeUp1071 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		while(true) {
			int x = sc.nextInt();
			if(x == 0) {
				break;
			}
			System.out.println(x);
		}
		sc.close();
	} // end main()

} // end CodeUp1070

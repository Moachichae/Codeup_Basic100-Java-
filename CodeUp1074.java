package basicProblem100.solving02;

import java.util.Scanner;

public class CodeUp1074 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int x = sc.nextInt();
		while(x>0) {
			System.out.println(x);
			x--;
		}
		sc.close();
	} // end main()

} // end CodeUp1074
